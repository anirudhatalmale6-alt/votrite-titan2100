        private void btn_VK_Click(object sender, EventArgs e)
        {
            SetFocusToInput();
            var button = sender as Button;
            switch (button.Name)
            {
                case "btn_VK_SPACE":
                    SendKeys.Send(" ");
                    return;

                case "btn_VK_BACKSPACE":
                    SendKeys.Send("{BS}");
                    return;
            }
            SendKeys.Send(button.Name.Substring(7).ToUpper());
        }

        private void btnCancel_Click(object sender, EventArgs e)
        {
