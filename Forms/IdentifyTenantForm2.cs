            Close();
        }

        private void btnSelect_Click(object sender, EventArgs e)
        {
            if (dgvTenants.SelectedRows.Count != 0)
            {
                int index =
                    tableSource.Rows.IndexOf((dgvTenants.SelectedRows[0].DataBoundItem as DataRowView).Row);
                SelectedTenant = Tenants.ElementAt(index);
                DialogResult = DialogResult.OK;
                Close();
            }
        }

